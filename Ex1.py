indice = 13
soma = 0
k = 0
while k < indice:
    k = k + 1
    soma = soma + k
    print(soma)
#Enquanto k < indice, o programa vai continuar rodando até que seja atingido o valor de indice "13", 
#por isso o valor de soma no final do processamento será 91.